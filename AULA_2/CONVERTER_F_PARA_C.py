#converter F para C

tempF = int(input("Temperatura em fahrenheit: "))
tempC = (((5* tempF)-32)/9)
print("A temperatura em Celsius: {} ".format(tempC))
print("A temperatura em Celsius: ",tempC)
print("A temperatura em Celsius: %f" %(tempC))
print("A temperatura em Celsius: {tempC}")
print(tempC)
print("A temperatura em Celsius: {tempC:.2f}")

